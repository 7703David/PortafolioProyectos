package Empresa;


public class Empresa {
	
	String Nombre;
	String Apellido;
	String Cargo;
	int Edad;
	String Genero;
	
	public Empresa(String Nombre, String Apellido,String Cargo, int Edad, String Genero) {
		this.Nombre = Nombre;
		this.Apellido = Apellido;
		this.Cargo = Cargo;
		this.Edad = Edad;
		this.Genero = Genero;
	}

}
